class Operator
{
public static void main(String args[])
{
int a=16,b=12;
System.out.println("addition is "+(a+b));
System.out.println("substaction is "+(a-b));
System.out.println("multiple is "+(a*b));
System.out.println("inc is "+(++a-b--));
System.out.println("shift left is "+(a<<1));
System.out.println("shift right is "+(b>>1));
}
}
